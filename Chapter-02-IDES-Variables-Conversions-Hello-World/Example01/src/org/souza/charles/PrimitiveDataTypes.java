public class PrimitiveDataTypes {

	public static void main(String[] args) {
		int x = 10;
		double d = x;
		long l = x;
		float f = x;

		System.out.println(x);
		System.out.println(d);
		System.out.println(l);
		System.out.println(f);
	}
}
